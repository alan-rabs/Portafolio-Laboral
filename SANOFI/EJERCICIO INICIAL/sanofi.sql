SELECT 
    C.PRODUCTO_ID, 
    C.PRODUCTO_DESC, 
    C.FAMILIA, 
    R.VENTA, 
    R.UNIDADES
--from nos indica de donde provienen los datos, C es el alias para la tabla Catalogo y R para la tabla reporte    
FROM 
    CATALOGO C
-- realiza la union entre las tablas en base a Producto ID    
JOIN 
    REPORTE R ON C.PRODUCTO_ID = R.PRODUCTO_ID
_--filtro de busqueda para que solo nos aparezcan los elementos con el status de activo     
WHERE 
    C.STATUS = 'ACTIVO';
    