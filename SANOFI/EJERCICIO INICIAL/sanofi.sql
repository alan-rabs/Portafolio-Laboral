CREATE TABLE CATALOGO (
    PRODUCTO_ID INT PRIMARY KEY,
    PRODUCTO_DESC VARCHAR(255),
    STATUS VARCHAR(50),
    FAMILIA VARCHAR(100)
);

CREATE TABLE REPORTE (
    PRODUCTO_ID INT,
    PRODUCTO_DESC VARCHAR(255),
    VENTA DECIMAL(10, 2),
    UNIDADES INT,
    FOREIGN KEY (PRODUCTO_ID) REFERENCES CATALOGO(PRODUCTO_ID)
);

